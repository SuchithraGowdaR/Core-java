package com.xworks.oops.encapsulation;

public class CarDTO {
	private String brand;
	private String modale;
	private String headLamp;
	private String colour;
	private int mxspeed;
	private int seat;
	private int airBag;
	private int price;
	private int numOgGear;
	private float enginCapacity;
	
	public CarDTO(){
		
	}

	public String getBrand() {
		return brand;
	}

	public void setBrand(String brand) {
		this.brand = brand;
	}

	public String getModale() {
		return modale;
	}

	public void setModale(String modale) {
		this.modale = modale;
	}

	public String getHeadLamp() {
		return headLamp;
	}

	public void setHeadLamp(String headLamp) {
		this.headLamp = headLamp;
	}

	public String getColour() {
		return colour;
	}

	public void setColour(String colour) {
		this.colour = colour;
	}

	public int getMxspeed() {
		return mxspeed;
	}

	public void setMxspeed(int mxspeed) {
		this.mxspeed = mxspeed;
	}

	public int getSeat() {
		return seat;
	}

	public void setSeat(int seat) {
		this.seat = seat;
	}

	public int getAirBag() {
		return airBag;
	}

	public void setAirBag(int airBag) {
		this.airBag = airBag;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getNumOgGear() {
		return numOgGear;
	}

	public void setNumOgGear(int numOgGear) {
		this.numOgGear = numOgGear;
	}

	public float getEnginCapacity() {
		return enginCapacity;
	}

	public void setEnginCapacity(float enginCapacity) {
		this.enginCapacity = enginCapacity;
	}
	

		
}
